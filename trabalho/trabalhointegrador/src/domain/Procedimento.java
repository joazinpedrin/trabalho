package domain;

public class Procedimento {

  private int codProced;
  private String termCarenc;
  private String tipoProced;
  public int getCodProced() {
    return codProced;
  }
  public void setCodProced(int codProced) {
    this.codProced = codProced;
  }
  public String getTermCarenc() {
    return termCarenc;
  }
  public void setTermCarenc(String termCarenc) {
    this.termCarenc = termCarenc;
  }
  public String getTipoProced() {
    return tipoProced;
  }
  public void setTipoProced(String tipoProced) {
    this.tipoProced = tipoProced;
  }
  
}
